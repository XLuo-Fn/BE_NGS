#!/bin/bash

echo "开始扫描当前目录下的所有文件夹..."

# 使用 */ 遍历当前目录下的所有文件夹
for folder in */; do
    
    # 检查是否为真实存在的目录（这是一个安全机制，防止当前目录下没有任何文件夹时报错）
    if [ -d "$folder" ]; then
        
        # 去除文件夹名字末尾的斜杠，让终端输出的信息更美观
        dir_name="${folder%/}"
        
        echo "===================================="
        echo "正在进入 $dir_name 并运行脚本..."
        
        # 进入文件夹，如果进入失败则中断退出，以保证安全
        cd "$folder" || exit
        
        # 运行上级目录的 b.sh
        sh ../barcode_split_tips.sh
        
        # 运行结束后返回上级目录
        cd ..
        
        echo "$dir_name 运行结束。"
    else
        echo "警告：未找到任何文件夹或跳过无效目标。"
    fi
done

echo "===================================="
echo "当前目录下所有文件夹的任务已全部执行完毕！"